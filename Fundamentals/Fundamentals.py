#  Create variables and introduction to datatypes:
#  Creating variable, no need define datatype and they are replaceable

# Integer
age = 12
print(age)

# String
name = "Newman"
print(name)

# Float/Decimal
price = 10.021
print(price)

# Boolean
isValue = True
print(isValue)

# Type conversion
beNum = int(21.3)
print(beNum)

beStr = str(23)
print(beStr)

beFloat = float(10)
print(beFloat)

beBool = bool("true")
print(beBool)
