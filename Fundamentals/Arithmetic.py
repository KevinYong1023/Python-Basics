# Basic Operators
print("Addition", 10 + 3)
print("Subtraction", 10 - 3)
print("Multiplication", 10 * 3)
print("Division return Decimal", 10 / 3)
print("Division return Integer", 10 // 3)
print("Modules operator", 10 % 3)  # return the remaining division
print("Power of a number", 10 ** 2)

# Augmented assignment
x = 3
x += 3
print(x)

# Operator Precedence
# Just like Basic Mathematics: Divide and Multiply first
o = 10 + 3 * 2  # = 16
print(o)
# Can restructure by putting bracket to prioritse certain
p = (10 + 3) * 2  # = 26
print(p)
